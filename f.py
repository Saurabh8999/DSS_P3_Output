# List slicing in Python
my_list = ['p', 'r', 'o', 'g', 'r', 'a', 'm', 'i', 'z']
# includes element at index 2, 3, 4
# excludes element at index 5
print(my_list[2:5])
# elements beginning to 4th
print(my_list[:-5])
# elements 6th to end
print(my_list[5:])
# elements beginning to end
print(my_list[:])

